package com.pismo.transactions.infrastructure.adapters.out.persistence;

import com.pismo.transactions.infrastructure.adapters.out.persistence.entity.TransactionEntity;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.data.jpa.test.autoconfigure.DataJpaTest;
import org.springframework.boot.jdbc.test.autoconfigure.AutoConfigureTestDatabase;
import org.springframework.boot.testcontainers.service.connection.ServiceConnection;
import org.testcontainers.containers.PostgreSQLContainer;
import org.testcontainers.junit.jupiter.Container;
import org.testcontainers.junit.jupiter.Testcontainers;

import java.math.BigDecimal;
import java.time.LocalDateTime;

import static org.junit.jupiter.api.Assertions.*;

@DataJpaTest(properties = {
        "spring.flyway.enabled=false",
        "spring.jpa.hibernate.ddl-auto=create-drop",
        "spring.jpa.show-sql=true"
})
@AutoConfigureTestDatabase(replace = AutoConfigureTestDatabase.Replace.NONE)
@Testcontainers
class TransactionRepositoryIT {

    @Container
    @ServiceConnection
    static PostgreSQLContainer<?> postgres = new PostgreSQLContainer<>("postgres:15-alpine");

    @Autowired
    private SpringDataTransactionRepository repository;

    @Test
    @DisplayName("Should persist transaction with correct database column names and retrieve ID")
    void shouldPersistTransaction() {
        TransactionEntity entity = new TransactionEntity();
        entity.setAccountId(1L);
        entity.setOperationTypeId(1);
        entity.setAmount(new BigDecimal("-100.00"));
        entity.setEventDate(LocalDateTime.now());

        TransactionEntity saved = repository.save(entity);

        assertNotNull(saved.getTransactionId(), "Transaction ID should be generated by the database");
        assertEquals(0, new BigDecimal("-100.00").compareTo(saved.getAmount()), "Saved amount should match input value");
        assertNotNull(saved.getEventDate(), "Event date should be persisted");
    }
}