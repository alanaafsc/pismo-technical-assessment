package com.pismo.transactions.infrastructure.adapters.out.persistence;

import com.pismo.transactions.infrastructure.adapters.out.persistence.entity.AccountEntity;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.data.jpa.test.autoconfigure.DataJpaTest;
import org.springframework.boot.jdbc.test.autoconfigure.AutoConfigureTestDatabase;
import org.springframework.boot.testcontainers.service.connection.ServiceConnection;
import org.testcontainers.containers.PostgreSQLContainer;
import org.testcontainers.junit.jupiter.Container;
import org.testcontainers.junit.jupiter.Testcontainers;

import static org.junit.jupiter.api.Assertions.*;

@DataJpaTest(properties = {
        "spring.flyway.enabled=false",
        "spring.jpa.hibernate.ddl-auto=create-drop",
        "spring.jpa.show-sql=true"
})
@AutoConfigureTestDatabase(replace = AutoConfigureTestDatabase.Replace.NONE)
@Testcontainers
class AccountRepositoryIT {

    @Container
    @ServiceConnection
    static PostgreSQLContainer<?> postgres = new PostgreSQLContainer<>("postgres:15-alpine");

    @Autowired
    private SpringDataAccountRepository repository;

    @Test
    @DisplayName("Should persist an account and generate an ID")
    void shouldPersistAccount() {
        AccountEntity entity = new AccountEntity();
        entity.setDocumentNumber("12345678900");

        AccountEntity saved = repository.save(entity);

        assertNotNull(saved.getAccountId(), "Account ID should be generated by the database");
        assertEquals("12345678900", saved.getDocumentNumber(), "Document number should match the input");
    }
}
